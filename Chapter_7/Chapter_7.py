# Escape Sequence Characters
print("New line\nNew Line")
print("New line\bNew Line")   # \b is used to do backspace
print("New line\"New Line") # Double Quotes using Escape Sequence character
print("New line\'New Line") # Single Quote using Escape Sequence character
print("New line\rNew Line") #  Carriage Return - Moves the cursor to beginning
print("C:\\Users\\Dhrubajyoti Biswas\\Videos\\Captures")   # backslash escape sequence character
print("Hello \t World")  # Tab is used for indentation
